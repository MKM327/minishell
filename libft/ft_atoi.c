#include "libft.h"

long long	ft_atoi(const char *str)
{
	int			i;
	int			neg;
	long long	sayi;

	i = 0;
	neg = 1;
	sayi = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sayi = sayi * 10 + (str[i] - 48);
		i++;
	}
	return (sayi * neg);
}
