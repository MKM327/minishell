#include "../execute.h"

int	pwd(char **env)
{
	int	i;

	i = 0;
	while (env[i] != NULL)
	{
		if (ft_strncmp(env[i], "PWD=", 4) == 0)
		{
			printf("%s\n", env[i] + 4);
			return (1);
		}
		i++;
	}
	return (1);
}
